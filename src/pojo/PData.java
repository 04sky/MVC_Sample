package pojo;

public class PData {
	private String pType;
	private Integer number;
	
	
	public PData() {
		super();
	}


	public PData(String pType, Integer number) {
		super();
		this.pType = pType;
		this.number = number;
	}


	public String getpType() {
		return pType;
	}


	public void setpType(String pType) {
		this.pType = pType;
	}


	public Integer getNumber() {
		return number;
	}


	public void setNumber(Integer number) {
		this.number = number;
	}

	
	
}
